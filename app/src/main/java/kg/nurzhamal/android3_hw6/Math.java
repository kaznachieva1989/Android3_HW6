package kg.nurzhamal.android3_hw6;

public class Math {

    public int add(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int division (int a, int b) {
        return a/b;
    }
}
